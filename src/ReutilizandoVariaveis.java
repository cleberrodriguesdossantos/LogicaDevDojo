
public class ReutilizandoVariaveis {

	public static void main(String[] args) {
		double salario = 5000;
		double resultado = salario * 0.3;
		System.out.println(" 30% é igual a: "+resultado);
		
		resultado = salario * 0.15;
		System.out.println(" 15% é igual a: "+resultado);
		
		resultado = salario * 0.05;
		System.out.println(" 5% é igual a: "+resultado);

	}

}
